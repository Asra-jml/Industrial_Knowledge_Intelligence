"""Build the YOLO dataset config from the Digitize-PID corpus folder.

The corpus already uses the Ultralytics layout (01_pids/images/*.jpg with
01_pids/labels/*.txt sidecars), so we only emit train/val path lists and a
data.yaml. No class-name file ships with the dataset, so classes get generic
names (symbol_0..symbol_N); nc = max class id + 1 discovered from the labels.

    python -m backend.cv.prepare_dataset
"""
from __future__ import annotations

from pathlib import Path

from backend.core import config

CV_DIR = Path(__file__).resolve().parent
DATASET_DIR = CV_DIR / "dataset"
VAL_EVERY = 10   # every 10th image -> validation split (deterministic)


def main() -> Path:
    images_dir = config.CORPUS_ROOT / "01_pids" / "images"
    labels_dir = config.CORPUS_ROOT / "01_pids" / "labels"
    images = sorted(images_dir.glob("*.jpg"), key=lambda p: int(p.stem))
    paired = [p for p in images if (labels_dir / f"{p.stem}.txt").exists()]

    max_class = 0
    for img in paired:
        for line in (labels_dir / f"{img.stem}.txt").read_text().splitlines():
            parts = line.split()
            if parts:
                max_class = max(max_class, int(parts[0]))
    nc = max_class + 1

    train = [p for i, p in enumerate(paired) if i % VAL_EVERY != 0]
    val = [p for i, p in enumerate(paired) if i % VAL_EVERY == 0]

    DATASET_DIR.mkdir(parents=True, exist_ok=True)
    (DATASET_DIR / "train.txt").write_text("\n".join(str(p) for p in train), encoding="utf-8")
    (DATASET_DIR / "val.txt").write_text("\n".join(str(p) for p in val), encoding="utf-8")

    names = "\n".join(f"  {i}: symbol_{i}" for i in range(nc))
    yaml_path = DATASET_DIR / "data.yaml"
    yaml_path.write_text(
        f"# generated by prepare_dataset.py — Digitize-PID symbols (no official class names)\n"
        f"path: {DATASET_DIR}\n"
        f"train: train.txt\n"
        f"val: val.txt\n"
        f"nc: {nc}\n"
        f"names:\n{names}\n",
        encoding="utf-8",
    )
    print(f"[cv] {len(train)} train / {len(val)} val images, {nc} symbol classes")
    print(f"[cv] wrote {yaml_path}")
    return yaml_path


if __name__ == "__main__":
    main()
